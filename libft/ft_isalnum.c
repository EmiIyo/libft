#include "libft.h"

int	ft_isalnum(int isal)
{
	if ((isal >= 'a' && isal <= 'z')
		|| (isal >= 'A' && isal <= 'Z')
		|| (isal >= '0' && isal <= '9'))
		return (1);
	return (0);
}
